    public void dataEmpleados() {
        try {
            resultado = db.consultaEmpleado();
            while(resultado.next()) {
                Object[] Usuario = {resultado.getString("idEmpleado"), resultado.getString("nombre"), resultado.getString("appat"), resultado.getString("apmat"), resultado.getString("domicilio"), resultado.getString("rfc"), resultado.getString("nss")};
                modelo.addRow(Usuario);
            }
            db.desconectar();
        } catch (SQLException e) {
            System.out.println(e.getMessage());
        }
    }
