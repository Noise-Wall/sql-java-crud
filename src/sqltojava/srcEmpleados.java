    private void tblEmpleadoMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblEmpleadoMouseClicked
        DefaultTableModel model = (DefaultTableModel)tblEmpleado.getModel();
        int fila = tblEmpleado.getSelectedRow();
        
        txtIdEmpleado.setText(model.getValueAt(fila, 0).toString());
        txtNombre.setText(model.getValueAt(fila, 1).toString());
        txtApPat.setText(model.getValueAt(fila, 2).toString());
        txtApMat.setText(model.getValueAt(fila, 3).toString());
        txtDomicilio.setText(model.getValueAt(fila, 4).toString());
        txtRfc.setText(model.getValueAt(fila, 5).toString());
        txtNss.setText(model.getValueAt(fila, 6).toString());
        
        // obtener estado
        tglEstado.setEnabled(true);
        if (db.obtenerEstado(model.getValueAt(fila,0).toString())) {
            tglEstado.setText("Activo");
            tglEstado.setSelected(true);
        } else {
            tglEstado.setText("Inactivo");
            tglEstado.setSelected(false);
        }
    }//GEN-LAST:event_tblEmpleadoMouseClicked

    private void btnActualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnActualizarActionPerformed
        db.updateEmpleado(txtIdEmpleado.getText(), txtNombre.getText(), txtApPat.getText(), txtApMat.getText(), txtDomicilio.getText(), txtRfc.getText(), txtNss.getText());
        modelo.setRowCount(0);
        dataEmpleados();
    }//GEN-LAST:event_btnActualizarActionPerformed

    private void tglEstadoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tglEstadoActionPerformed
        if (tglEstado.isSelected()) {
            tglEstado.setText("Activo");
            db.actualizarEstado(txtIdEmpleado.getText(), "1");
        } else {
            tglEstado.setText("Inactivo");
            db.actualizarEstado(txtIdEmpleado.getText(), "0");
        }   
    }//GEN-LAST:event_tglEstadoActionPerformed

